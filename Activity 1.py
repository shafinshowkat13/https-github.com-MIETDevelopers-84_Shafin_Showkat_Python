# Built-in functions

# abs() function
x=abs(-2.35)
print(x)

# len() function
x=len("Hello World")
print(x)

# min() function
x=min(1,11)
print(x)

# max() function
x=max(2,5)
print(x)

# round() function
x=round(10.60)
print(x)

# isalnum() function
txt="CSEDeptt"
x=txt.isalnum()
print(x)

# type() function
a=2
print(a, "is of type", type(a))

b=9.0
print(b, "is of type", type(b))

c=2+5j
print(c, "is of type", type(c))